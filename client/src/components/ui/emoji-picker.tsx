interface EmojiPickerProps {
  onEmojiSelect: (emoji: string) => void;
  className?: string;
}

export function EmojiPicker({ onEmojiSelect, className }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState(0);
  const [isOpen, setIsOpen] = useState(false);
  const containerRef = useRef<HTMLDivElement>(null);

  // Close emoji picker when clicking outside
  useEffect(() => {
    const handleClickOutside = (event: MouseEvent) => {
      if (containerRef.current && !containerRef.current.contains(event.target as Node)) {
        setIsOpen(false);
      }
    };

    document.addEventListener('mousedown', handleClickOutside);
    return () => {
      document.removeEventListener('mousedown', handleClickOutside);
    };
  }, []);

  return (
    <Popover open={isOpen} onOpenChange={setIsOpen}>
      <PopoverTrigger asChild>
        <Button 
          variant="ghost" 
          size="icon"
          className={cn("h-8 w-8", className)}
          onClick={() => setIsOpen(!isOpen)}
        >
          <Smile className="h-5 w-5" />
        </Button>
      </PopoverTrigger>
      <PopoverContent 
        ref={containerRef}
        className="w-80 p-0" 
        align="end"
        sideOffset={5}
      >
        <div className="border-b p-2 flex justify-between gap-1 overflow-x-auto">
          {emojiCategories.map((category, index) => (
            <Button
              key={category.name}
              variant="ghost"
              size="sm"
              className={cn(
                "px-2 py-1 text-xs font-normal h-auto",
                activeCategory === index && "bg-muted"
              )}
              onClick={() => setActiveCategory(index)}
            >
              {category.name}
            </Button>
          ))}
        </div>
        
        <div className="p-2 h-52 overflow-y-auto">
          <div className="grid grid-cols-8 gap-1">
            {emojiCategories[activeCategory].emojis.map((emoji, index) => (
              <button
                key={index}
                className="p-1 text-xl hover:bg-muted rounded cursor-pointer transition-colors"
                onClick={() => {
                  onEmojiSelect(emoji);
                  setIsOpen(false);
                }}
                type="button"
              >
                {emoji}
              </button>
            ))}
          </div>
        </div>
      </PopoverContent>
    </Popover>
  );
}